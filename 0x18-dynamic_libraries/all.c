#include <stdio.h>
/**
 * add - add 2 nums
 * @k: one
 * @m: two
 * Return:  add
 */
int add(int k, int m)
{
	return(k + m);
}
/**
 * sub - sub 2 nums
 * @k: one
 * @m: two
 * Return:  sub
 */
int sub(int k, int m)
{
	return(k - m);
}
/**
 * mul - mul 2 nums
 * @k: one
 * @m: two
 * Return: mul
 */
int mul(int k, int m)
{
	return(k * m);
}
/**
 * div - div 2 nums
 * @k: one
 * @m: two
 * Return:  div
 */
int div(int k, int m)
{
	return(k / m);
}
/**
 * mod - mod 2 nums
 * @k: one
 * @m: two
 * Return:  mod
 */
int mod(int k, int m)
{
	return(k % m);
}
